<template>
<div :style='{"width":"1200px","padding":"20px","boxShadow":"0px 4px 10px 0px rgba(0,0,0,0.302)","margin":"10px auto","position":"relative","background":"#fff"}'>
    <el-form
      class="add-update-preview"
      ref="ruleForm"
      :model="ruleForm"
      :rules="rules"
      label-width="110px"
    >
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="党员账号" prop="dangyuanzhanghao">
            <el-select  @change="dangyuanzhanghaoChange" v-model="ruleForm.dangyuanzhanghao" placeholder="请选择党员账号">
              <el-option
                  v-for="(item,index) in dangyuanzhanghaoOptions"
                  :key="index"
                  :label="item"
                  :value="item">
              </el-option>
            </el-select>
          </el-form-item>
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="党员姓名" prop="dangyuanxingming">
            <el-input v-model="ruleForm.dangyuanxingming" 
                placeholder="党员姓名" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="党费" prop="dangfei">
            <el-input v-model="ruleForm.dangfei" 
                placeholder="党费" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="提交时间" prop="tijiaoshijian" >
              <el-date-picker
                  format="yyyy 年 MM 月 dd 日"
                  value-format="yyyy-MM-dd"
                  v-model="ruleForm.tijiaoshijian" 
                  type="date"
                  placeholder="提交时间">
              </el-date-picker> 
          </el-form-item>

      <el-form-item :style='{"padding":"0","margin":"0 40px"}'>
        <el-button :style='{"border":"0","cursor":"pointer","padding":"0","margin":"0 20px 0 0","outline":"none","color":"rgba(255, 255, 255, 1)","borderRadius":"4px","background":"#5c39bb","width":"128px","lineHeight":"40px","fontSize":"14px","height":"40px"}'  type="primary" @click="onSubmit">提交</el-button>
        <el-button :style='{"border":"0px solid rgba(64, 158, 255, 1)","cursor":"pointer","padding":"0","margin":"0","outline":"none","color":"#fff","borderRadius":"4px","background":"#f3a9a0","width":"128px","lineHeight":"40px","fontSize":"14px","height":"40px"}' @click="back()">返回</el-button>
      </el-form-item>
    </el-form>
</div>
</template>

<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            dangyuanzhanghao : false,
            dangyuanxingming : false,
            dangfei : false,
            tijiaoshijian : false,
            ispay : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          dangyuanzhanghao: '',
          dangyuanxingming: '',
          dangfei: '',
          tijiaoshijian: '',
          ispay: '',
        },
        dangyuanzhanghaoOptions: [],
        rules: {
          dangyuanzhanghao: [
          ],
          dangyuanxingming: [
          ],
          dangfei: [
            { required: true, message: '党费不能为空', trigger: 'blur' },
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          tijiaoshijian: [
          ],
          ispay: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.tijiaoshijian = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='dangyuanzhanghao'){
              this.ruleForm.dangyuanzhanghao = obj[o];
              this.ro.dangyuanzhanghao = true;
              continue;
            }
            if(o=='dangyuanxingming'){
              this.ruleForm.dangyuanxingming = obj[o];
              this.ro.dangyuanxingming = true;
              continue;
            }
            if(o=='dangfei'){
              this.ruleForm.dangfei = obj[o];
              this.ro.dangfei = true;
              continue;
            }
            if(o=='tijiaoshijian'){
              this.ruleForm.tijiaoshijian = obj[o];
              this.ro.tijiaoshijian = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.$http.get('option/dangyuan/dangyuanzhanghao', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.dangyuanzhanghaoOptions = res.data.data;
          }
        });
      },
      // 下二随
      dangyuanzhanghaoChange () {
        this.$http.get('follow/dangyuan/dangyuanzhanghao?columnValue=' + this.ruleForm.dangyuanzhanghao, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.dangyuanxingming){
              this.ruleForm.dangyuanxingming = res.data.data.dangyuanxingming
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('dangfeijiaona/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('dangfeijiaona/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('dangfeijiaona/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('dangfeijiaona/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

